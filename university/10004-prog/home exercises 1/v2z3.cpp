#include <iostream>
using namespace std;

int main()
{
	int brojA = 10;
	int brojB = 20;

	cout << brojA << "+" << brojB << "=" << brojA + brojB << endl;
	cout << brojA << "-" << brojB << "=" << brojA - brojB << endl;
	cout << brojA << "*" << brojB << "=" << brojA * brojB << endl;
	cout << brojA << "/" << brojB << "=" << brojA / brojB << endl;
	return 0;
}